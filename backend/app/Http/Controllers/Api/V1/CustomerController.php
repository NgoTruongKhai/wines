class CustomerController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        // return Customer::all();

        $filler = new CustomerFilter();
        $filterItems = $filler->transform($request);

        $includeOrders = $request->query('includeOrders');

        $customer = Customer::where($filterItems);

        if ($includeOrders) {
            $customer->with('orders');
        }

        return new CustomerCollection($customer->paginate()->appends($request->query()));
    }

